#include "pipex.h"

void	free_tab(char **tab)
{
	int	i;

	i = 0;
	if (!tab)
		return ;
	while (tab[i])
	{
		free(tab[i]);
		i++;
	}
	free(tab);
	tab = NULL;
}

int	exit_error(void)
{
	write(2, strerror(errno), ft_strlen(strerror(errno)));
	exit(0);
	return (1);
}